namespace Nucleos\SetlistFm\Model;

/**
 * @psalm-immutable
 */
final class Tour
{
    /**
     * @var string
     */
    private $name;

    public function __construct(string $name)
    {
        $this->name = $name;
    }

    public function getName(): string
    {
        return $this->name;
    }

    public static function fromApi(array $data): self
    {
        return new self(
            $data['name']
        );
    }
}
